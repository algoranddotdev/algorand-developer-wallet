import XCTest

class Sha512Slash256Tests: XCTestCase {
  
  func testDigestEmpty() {
    XCTAssertEqual(Sha512Slash256.hash(bytes: Array<UInt8>("".utf8)), Array<UInt8>([
      0xc6, 0x72, 0xb8, 0xd1, 0xef, 0x56, 0xed, 0x28,
      0xab, 0x87, 0xc3, 0x62, 0x2c, 0x51, 0x14, 0x06,
      0x9b, 0xdd, 0x3a, 0xd7, 0xb8, 0xf9, 0x73, 0x74,
      0x98, 0xd0, 0xc0, 0x1e, 0xce, 0xf0, 0x96, 0x7a
    ]))
  }
  func testSimpleBytes() {
    XCTAssertEqual(Sha512Slash256.hash(bytes: Array<UInt8>([
      0x01, 0x02, 0x04, 0x08
    ])), Array<UInt8>([
      218, 137, 250, 37, 19, 112, 49, 213,
      31, 9, 46, 236, 120, 91, 163, 106,
      100, 179, 121, 63, 156, 31, 185, 59,
      55, 245, 52, 135, 129, 181, 173, 143
    ]))
  }
  func testQuickBrownFox() {
    XCTAssertEqual(Sha512Slash256.hash(bytes: Array<UInt8>("The quick brown fox jumps over the lazy dog".utf8)), Array<UInt8>([
      0xdd, 0x9d, 0x67, 0xb3, 0x71, 0x51, 0x9c, 0x33,
      0x9e, 0xd8, 0xdb, 0xd2, 0x5a, 0xf9, 0x0e, 0x97,
      0x6a, 0x1e, 0xee, 0xfd, 0x4a, 0xd3, 0xd8, 0x89,
      0x00, 0x5e, 0x53, 0x2f, 0xc5, 0xbe, 0xf0, 0x4d
    ]))
  }
  
}
